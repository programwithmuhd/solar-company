    {
        $data = request()->validate([
            'name' => 'required',
            'email' => 'required|email',
            'contact' => 'required',
            'message' => 'required',
        ]);

        Mail::to('talktomuhd@gmail.com')->send(new ContactFormMail($data));

        return redirect()->route('thankyou.index')->with('success_message', 'Thank you');
    }

